{-# LANGUAGE RankNTypes #-}

module UNIT
  ( UNIT(..)
  , unit
  , id'
  ) where

----------------------------------------------------------
-- One Type
----------------------------------------------------------
type OneType = forall r. r -> r

----------------------------------------------------------
-- Constructors
----------------------------------------------------------
one' :: OneType
one' = \x -> x

id' :: forall a. a -> a
id' = one'

----------------------------------------------------------
-- UNIT Type
----------------------------------------------------------
newtype UNIT = UNIT { unUnit :: OneType }

----------------------------------------------------------
-- Constructors
----------------------------------------------------------
unit :: UNIT
unit = UNIT one'
