{-# LANGUAGE RankNTypes #-}

module UNIT
  ( id'
  , UNIT(..)
  , unit
  ) where

----------------------------------------------------------
-- One Type
----------------------------------------------------------
type OneType = forall r. r -> r

----------------------------------------------------------
-- Constructors
----------------------------------------------------------
one' :: OneType
one' = \x -> x

id' = one'
----------------------------------------------------------
-- UNIT Type
----------------------------------------------------------
newtype UNIT = UNIT { unUnit :: OneType }

----------------------------------------------------------
-- Constructors
----------------------------------------------------------
unit :: UNIT
unit = UNIT one'
