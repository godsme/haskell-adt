{-# LANGUAGE RankNTypes #-}

module UNIT
  ( UNIT(..)
  , unit
  , id'
  ) where

----------------------------------------------------------
-- One Type
----------------------------------------------------------
type OneType = forall r. r -> r

----------------------------------------------------------
-- Constructors
----------------------------------------------------------
one_1 :: OneType
one_1 = \x -> x

id' :: forall a. a -> a
id' = one_1

----------------------------------------------------------
-- UNIT Type
----------------------------------------------------------
newtype UNIT = UNIT { unUnit :: OneType }

----------------------------------------------------------
-- Constructors
----------------------------------------------------------
unit :: UNIT
unit = UNIT one_1
